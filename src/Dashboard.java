    public void LoadData(){
    
        String sql = "select * from LabUsers";
    try {
        pst = conn.prepareStatement(sql);
        rs = pst.executeQuery();
        Object[] columnData = new Object[4];
        while (rs.next()) {
            columnData[0] = rs.getString("First_Name");
            columnData[1] = rs.getString("Last_Name");
            columnData[2] = rs.getString("Username");
            columnData[3] = rs.getString("Password");
            defaultTableModel.addRow(columnData);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
        
    }
    
    }
    
