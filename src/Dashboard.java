    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
            String fname = jTextField1.getText();
            String sname = jTextField2.getText();
            String usernm = jTextField3.getText();
            String pass = jPasswordField1.getText();
            String pass1 = jPasswordField2.getText();
            
            
        try{
            String sql = "insert into LabUsers (First_Name,Last_Name,Username,Password) values (?,?,?,?)";
            pst = conn.prepareStatement(sql);
            if (usernm.equals("") || fname.equals("")|| sname.equals("") || pass.equals("") || pass1.equals("") && pass != pass1){
            JOptionPane.showMessageDialog(null, "Please Fill the empty field or Password Not Match");
            //rs.close();
            //pst.close();
            }else{
            pst.setString(1, jTextField1.getText());
            pst.setString(2, jTextField2.getText());
            pst.setString(3, jTextField3.getText());
            pst.setString(4, jPasswordField1.getText());
            
            pst.execute();
            JOptionPane.showMessageDialog(null, "New Account Created");
            pst.close();
            }
            
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jPasswordField1.setText("");
            jPasswordField2.setText("");
            
        
        
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

